public class operators {

    public static void main(String[] args) {
        int x = 10;
        int y = 20;
        int z = 30;
        int result = x + y - 2 / 2 + z;
        System.out.println(result);

        // operators are basic arithmetic operations that can be done on numberical values
        // if any operation is done on a string, the result will be a string
        // the + operator can be used to concatenate strings
        // if any opration is done with a int and a double/float, the result will be a double/float
        // boolean operators are used to compare two values and gives values of true or false
        // logical operators consist of &&, ||, and !
    }
    
}
